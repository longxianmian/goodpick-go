export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    // 【优先级1】检查 URL 参数 ?lang=xxx（最高优先级，用于测试和分享）
    const urlParams = new URLSearchParams(window.location.search);
    const urlLang = urlParams.get('lang');
    if (urlLang && ['zh-cn', 'en-us', 'th-th'].includes(urlLang.toLowerCase())) {
      const normalizedLang = urlLang.toLowerCase() as Language;
      console.log(`[语言初始化] 使用URL参数: ${normalizedLang}`);
      localStorage.setItem('language', normalizedLang);
      return normalizedLang;
    }

    // 【优先级2】检查 localStorage（用户手动选择过的语言）
    const stored = localStorage.getItem('language');
    if (stored && ['zh-cn', 'en-us', 'th-th'].includes(stored)) {
      console.log(`[语言初始化] 使用localStorage: ${stored}`);
      return stored as Language;
    }

    // 【优先级3】浏览器语言自动检测
    const browserLang = navigator.language || navigator.languages?.[0];
    if (browserLang) {
      const normalized = browserLang.toLowerCase();
      let detectedLang: Language | null = null;
      
      // 中文检测（支持 zh, zh-CN, zh-Hans, zh-TW, zh-Hant 等）
      if (normalized.startsWith('zh')) {
        detectedLang = 'zh-cn';
      }
      // 英文检测（支持 en, en-US, en-GB 等）
      else if (normalized.startsWith('en')) {
        detectedLang = 'en-us';
      }
      // 泰语检测（支持 th, th-TH 等）
      else if (normalized.startsWith('th')) {
        detectedLang = 'th-th';
      }
      
      if (detectedLang) {
        console.log(`[语言初始化] 检测到浏览器语言 ${browserLang}，使用: ${detectedLang}`);
        localStorage.setItem('language', detectedLang);
        return detectedLang;
      }
    }

    // 【优先级4】默认泰语（系统在泰国运营）
    const defaultLang: Language = 'th-th';
    console.log(`[语言初始化] 使用默认语言: ${defaultLang}`);
    localStorage.setItem('language', defaultLang);
    return defaultLang;
  });

  useEffect(() => {
    // 确保localStorage与React state保持同步
    const stored = localStorage.getItem('language');
    if (stored !== language) {
      console.log(`[同步修复] localStorage(${stored}) 与 state(${language}) 不一致，立即修复`);
      localStorage.setItem('language', language);
    }
  }, [language]);

  // 监听其他标签页的语言切换（跨标签页同步）
  useEffect(() => {
    const handleStorageChange = (e: StorageEvent) => {
      if (e.key === 'language' && e.newValue && ['zh-cn', 'en-us', 'th-th'].includes(e.newValue)) {
        console.log(`[跨窗口同步] 检测到其他窗口切换语言: ${e.oldValue} -> ${e.newValue}`);
        setLanguageState(e.newValue as Language);
        // 强制清除所有缓存并重新获取
        queryClient.clear();
        console.log(`[跨窗口同步] 已清除所有查询缓存，页面会自动重新获取数据`);
      }
    };
    window.addEventListener('storage', handleStorageChange);
    return () => window.removeEventListener('storage', handleStorageChange);
  }, []);

  const setLanguage = (lang: Language) => {
    console.log(`[setLanguage] 切换语言: ${lang}`);
    const oldValue = localStorage.getItem('language');
    console.log(`[setLanguage] 切换前localStorage: ${oldValue}`);
    
    // 立即同步保存到localStorage
    localStorage.setItem('language', lang);
    console.log(`[setLanguage] localStorage已立即保存为: ${localStorage.getItem('language')}`);
    
    // 手动触发storage事件，让其他窗口（包括Replit预览窗口）能收到
    const storageEvent = new StorageEvent('storage', {
      key: 'language',
      oldValue: oldValue,
      newValue: lang,
      url: window.location.href,
      storageArea: localStorage
    });
    window.dispatchEvent(storageEvent);
    console.log(`[setLanguage] 已手动触发storage事件，其他窗口会收到通知`);
    
    // 更新当前窗口的state
    setLanguageState(lang);
    // 语言切换时清除所有查询缓存，确保重新获取翻译后的内容
    queryClient.invalidateQueries();
  };

  const t = (key: string, params?: Record<string, string>): string => {
    let text = translations[language][key] || key;
    
    if (params) {
      Object.entries(params).forEach(([param, value]) => {
        text = text.replace(`{${param}}`, value);
      });
    }
    
    return text;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}
